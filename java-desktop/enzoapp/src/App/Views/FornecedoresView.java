    private Object getSelectedObject() {
        Object selecionado = null;
        int linhaSelecionada = grdLista.getSelectedRow();
        if (linhaSelecionada >= 0) {
            selecionado = list.get(linhaSelecionada);
        } else {
            JOptionPane.showMessageDialog(this, "Por gentileza, selecione um fornecedor.");
        }
        return selecionado;
    }
    
    public void Filtro(String filtro) {
        TableRowSorter sorter = new TableRowSorter(modelo);
        String regex = ".*" + filtro + ".*";
        sorter.setRowFilter(RowFilter.regexFilter(regex));
        grdLista.setRowSorter(sorter);

    }
    
    private void btnExcluirFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirFornecedorActionPerformed
        Object o = getSelectedObject();
        
        if (o != null) {
            f = (Fornecedores) o;
            String opcoes[] = {"Sim", "Não"};
            int opcao = JOptionPane.showOptionDialog(this, "Deseja remover esta forncedor: " + f.getNomeFantasia()+ "?",
                    "Confirma remoção", 0, JOptionPane.QUESTION_MESSAGE, null, opcoes, opcoes[1]);
            if (opcao == 0) {
                generic.delete(f);
                atualizaLista();
            }
        } 
    }//GEN-LAST:event_btnExcluirFornecedorActionPerformed

