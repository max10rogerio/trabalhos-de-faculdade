    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        try {
            int id = valorTabela();
            ClientesForm cf = new ClientesForm();
            cf.setTitle("Alterar Cadastro");
            cf.setId(id);
            cf.setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Selecione um usuário");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            int id = valorTabela();
            String mensagem = "Deseja Deletar" + id;
            int pergunta = JOptionPane.showConfirmDialog(null, mensagem, "Confirmar", JOptionPane.YES_NO_OPTION);
            if (pergunta == JOptionPane.YES_OPTION) {
                CRUD.Deletar(id);
                JOptionPane.showMessageDialog(null, "Deletado o usuário: " + id);
                listaUsuarios();
            }
        } catch (Exception IndexOutOfBoundsException) {
            JOptionPane.showMessageDialog(null, "Selecione um usuário");
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

