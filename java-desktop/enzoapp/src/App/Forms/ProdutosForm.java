    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        Produtos produto = new Produtos();
        String nome = nomeTexto.getText();
        String marca = marcaTexto.getText();
        int quantidade = Integer.parseInt(quantidadeTexto.getText());
        float valor = Float.parseFloat(valorTexto.getText());
        String fornecedor = fornecedorTexto.getSelectedItem().toString();
        //Date data = new Date();
       
            produto.setNome(nome);
            produto.setMarca(marca);
            produto.setQuantidade(quantidade);
            produto.setValorDeCompra(valor);
            produto.setFornecedor(fornecedor);

            ProdutosCRUD.Cadastrar(produto);
            JOptionPane.showMessageDialog(null, "Sucesso dados Cadastrados");
            dispose();

        
    }//GEN-LAST:event_salvarActionPerformed

