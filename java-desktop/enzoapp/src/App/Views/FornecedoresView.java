    public void atualizaLista() {        
        String[] nomesColunas = {"ID", "Nome Fantasia", "Razão Social", "CNPJ"};        
        list = generic.getAll("fornecedores");        
        
        Object[][] dadosArray = new Object[list.size()][nomesColunas.length];
                
        for (int i = 0; i < list.size(); i++) {
            f = (Fornecedores) list.get(i);
            dadosArray[i][0] = f.getId();
            dadosArray[i][1] = f.getNomeFantasia();
            dadosArray[i][2] = f.getRazaoSocial();
            dadosArray[i][3] = f.getCNPJ();
        }                
        DefaultTableModel dm = new DefaultTableModel(dadosArray, nomesColunas);
        grdLista.setModel(dm);        
    }
