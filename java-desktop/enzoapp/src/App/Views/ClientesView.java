    public int valorTabela() {
        Object row = tabela.getValueAt(tabela.getSelectedRow(), 0);
        String id = row.toString();
        int idCliente = Integer.parseInt(id);
        return idCliente;
    }

    public void Filtro(String filtro) {
        TableRowSorter sorter = new TableRowSorter(modelo);
        String regex = ".*" + filtro + ".*";
        sorter.setRowFilter(RowFilter.regexFilter(regex));
        tabela.setRowSorter(sorter);

    }

    private void listaUsuarios() {
        // Usuarios s = new Usuarios();
        String[] nomesColunas = {"Codigo", "Nome", "Cidade", "Telefone", "Cidade"};
        List todosUsers = CRUD.Listar();
        Object[][] dadosArray = new Object[todosUsers.size()][nomesColunas.length];
        for (int i = 0; i < todosUsers.size(); i++) {
            Usuarios usuario = (Usuarios) todosUsers.get(i);
            dadosArray[i][0] = usuario.getId();
            dadosArray[i][1] = usuario.getNome();
            dadosArray[i][2] = usuario.getEndereco();
            /* dadosArray[i][3] = usuario.getData();*/
            dadosArray[i][3] = usuario.getTelefone();
            dadosArray[i][4] = usuario.getCidade();
        }
        modelo = new DefaultTableModel(dadosArray, nomesColunas);
        tabela.setModel(modelo);
    }

